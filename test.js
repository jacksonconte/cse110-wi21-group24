/**
 * Adds two numbers together
 * @method
 * @param {Number} num1
 * @param {Number} num2
 * @returns {Number} sum of the two numbers
 */
function addNum(num1, num2) {
  return num1 + num2;
}

console.log(addNum(4, 2));

/**
 * Subtracts two numbers
 * @method
 * @param {Number} num1
 * @param {Number} num2
 * @returns {Number} difference of the two numbers
 */
function subNum(num1, num2) {
  return num1 - num2;
}

console.log(subNum(4, 2));

/**
 * Multiplies two numbers
 * @method
 * @param {Number} num1
 * @param {Number} num2
 * @returns {Number} product of the two numbers
 */
function multNum(num1, num2) {
  return num1 * num2;
}

console.log(multNum(4, 2));

/**
 * Divides two numbers
 * @method
 * @param {Number} num1
 * @param {Number} num2
 * @returns {Number} quotient of the two numbers
 */
function divNum(num1, num2) {
  return num1 / num2;
}

console.log(divNum(4, 2));
